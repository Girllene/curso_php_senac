// verificar se o numero e par ou impar

numero = 25


numero/2

// operador que pega o resto e chamado de modulo - operador %(pega o resto da divisão)
// usando o desvio condicional
resto = numero % 2

// boas práticas para o if
if (resto == 0) {
    console.log (numero + ' É um número par')
}
else {
    (console.log (numero + ' É um número ímpar'))
}



